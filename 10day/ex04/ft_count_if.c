int		ft_count_if(char **tab, int (*f)(char *))
{
	int i;
	int summ;

	i = 0;
	summ = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
			summ++;
		i++;
	}
	return (summ);
}
