int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;

	i = 0;
	while ((s1[i] == s2[i]) && --n)
	{
		if (s1[i] == '\0')
		{
			return (0);
		}
		i++;
	}
	if ((s1[i] - s2[i]) > 0)
	{
		return (1);
	}
	if ((s1[i] - s2[i]) < 0)
	{
		return (-1);
	}
	return (0);
}
